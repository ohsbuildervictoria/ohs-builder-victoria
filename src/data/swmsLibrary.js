// Quick lookup by trade name (case-insensitive partial match)
export function findSwms(tradeName) {
  if (!tradeName) return null;
  const lower = tradeName.toLowerCase();
  return swmsLibrary.find(s =>
    s.trade.toLowerCase().includes(lower) ||
    lower.includes(s.trade.toLowerCase().split('/')[0].trim().toLowerCase())
  );
}
